"""Handler for document_complete WebSocket event - dispatches to tool-specific handlers by tool_type."""

import json
import os
import uuid
from typing import Any, cast

from app.infra.v4.activity.websocket_logger import log_websocket_activity
from app.infra.v4.websocket.get_db_connection import get_db_connection
from app.infra.v4.websocket.handler_wrapper import handle_internal_event
from app.infra.v4.websocket.openapi_helpers import register_server_endpoint
from app.infra.v4.websocket.typed_emit import emit_to_client, emit_to_internal
from app.main import UPLOAD_FOLDER, get_internal_sio, sio
from fastapi import APIRouter
from utils.cache.invalidate_tags import invalidate_tags
from utils.sql_helper import execute_sql_typed

internal_sio = get_internal_sio()

server_router = APIRouter()

# Import autogenerated types
from app.sql.types import (DocumentGenerationCompleteApiRequest,
                           DocumentGenerationCompleteSqlParams,
                           DocumentGenerationCompleteSqlRow,
                           DocumentGenerationErrorApiRequest,
                           GetDocumentToolCallResultsSqlParams,
                           GetDocumentToolCallResultsSqlRow)

SQL_GET_TOOL_RESULTS_PATH = "app/sql/v4/documents/get_document_tool_call_results_complete.sql"
SQL_COMPLETE_PATH = "app/sql/v4/documents/document_generation_complete_complete.sql"

# Map tool_type enum to event name (stable mapping based on enum)
TOOL_TYPE_COMPLETE_EVENT_MAP = {
    "title": "document_tool_title_complete",
    "html": "document_tool_html_complete",
    "schema": "document_tool_schema_complete",
}


async def _document_complete_impl(
    sid: str,
    data: dict[str, Any],  # Event payload - will be validated by handler wrapper
    profile_id: uuid.UUID,
    group_id: uuid.UUID | None = None,
) -> None:
    """Dispatch to tool-specific complete handler by tool_type or handle run_complete."""
    event_type = data.get("type") if isinstance(data, dict) else getattr(data, "type", None)
    if event_type == "tool_call_complete":
        # Tool-specific completion - route to tool-specific handlers
        # Note: tool_type should be passed from generate.py, but we handle fallback
        tool_type = getattr(data, "tool_type", None)
        if not tool_type:
            # Fallback: try to get from tool_name (for backward compatibility)
            tool_name = getattr(data, "tool_name", None)
            if tool_name == "create_title":
                tool_type = "title"
            elif tool_name == "generate_html":
                tool_type = "html"
            elif tool_name == "generate_schema":
                tool_type = "schema"

        # Route based on tool_type (stable enum)
        tool_event_name = (
            TOOL_TYPE_COMPLETE_EVENT_MAP.get(tool_type) if tool_type else None
        )

        if tool_event_name:
            await internal_sio.emit(
                tool_event_name,
                {
                    "sid": sid,
                    "document_id": data.document_id,
                    "run_id": data.run_id,
                    "tool_call_id": getattr(data, "tool_call_id", "") or "",
                    "call_id": getattr(data, "call_id", None),
                },
            )
        else:
            document_id_for_error = data.get("document_id") if isinstance(data, dict) else getattr(data, "document_id", None)
            await emit_to_internal(
                "document_error",
                DocumentGenerationErrorApiRequest(
                    error_message=f"Unknown tool_type for completion: {tool_type}",
                    document_id=document_id_for_error,
                    group_id=str(group_id) if group_id else None,
                ),
                sid=sid,
            )

    elif event_type == "run_complete":
        # All tools done - extract results and create templates using comprehensive SQL function
        run_id_str = data.get("run_id") if isinstance(data, dict) else getattr(data, "run_id", None)
        document_id_str = data.get("document_id") if isinstance(data, dict) else getattr(data, "document_id", None)
        department_id_str = data.get("department_id") if isinstance(data, dict) else getattr(data, "department_id", None)
        document_name = data.get("document_name") if isinstance(data, dict) else getattr(data, "document_name", None)
        
        if not run_id_str:
                    await emit_to_internal(
                        "document_error",
                        DocumentGenerationErrorApiRequest(
                            error_message="Missing run_id in run_complete event",
                            document_id=document_id_str,
                            group_id=str(group_id) if group_id else None,
                        ),
                        sid=sid,
                    )
                    return

        try:
            async with get_db_connection() as conn:
                model_run_id = uuid.UUID(run_id_str)
                document_id = uuid.UUID(document_id_str) if document_id_str else None

                # 1. Extract tool results using SQL function
                tool_results_params = GetDocumentToolCallResultsSqlParams(
                    run_id=model_run_id
                )
                tool_results = cast(
                    GetDocumentToolCallResultsSqlRow,
                    await execute_sql_typed(
                        conn, SQL_GET_TOOL_RESULTS_PATH, params=tool_results_params
                    ),
                )

                template_html = tool_results.template_html or ""
                template_schema_json = tool_results.template_schema_json or "{}"

                # Verify both tools were called and have results
                if not template_html or not template_schema_json or template_schema_json == "{}":
                    await emit_to_internal(
                        "document_error",
                        DocumentGenerationErrorApiRequest(
                            error_message=(
                                "Document agent did not call both required tools or tools returned empty results. "
                                "Expected: generate_html and generate_schema"
                            ),
                            document_id=document_id_str,
                            group_id=str(group_id) if group_id else None,
                        ),
                        sid=sid,
                    )
                    return

                # Parse schema JSON for validation
                try:
                    template_schema = json.loads(template_schema_json)
                except (json.JSONDecodeError, TypeError):
                    template_schema = {}

                # 2. Write template HTML to file (file system operation)
                upload_uuid = uuid.uuid4()
                file_path = f"{upload_uuid}.html"
                full_path = os.path.join(UPLOAD_FOLDER, file_path)

                # Ensure uploads directory exists
                os.makedirs(UPLOAD_FOLDER, exist_ok=True)

                # Write template HTML to file
                with open(full_path, "w", encoding="utf-8") as f:
                    f.write(template_html)

                file_size = len(template_html.encode("utf-8"))

                # 3. Call comprehensive SQL function - does ALL database work
                complete_params = DocumentGenerationCompleteSqlParams(
                    profile_id=profile_id,
                    run_id=model_run_id,
                    file_path=file_path,
                    file_size=file_size,
                    template_html=template_html,
                    template_schema_json=template_schema_json,
                    document_id=document_id,
                    group_id=group_id,
                    department_id=uuid.UUID(department_id_str) if department_id_str else None,
                    document_name=document_name,
                )
                complete_result = cast(
                    DocumentGenerationCompleteSqlRow,
                    await execute_sql_typed(conn, SQL_COMPLETE_PATH, params=complete_params),
                )

                # 4. Invalidate cache if document was created
                if document_id:
                    await invalidate_tags(["documents"])

                # 5. Emit final completion to client using typed payload
                await emit_to_client(
                    "documents_complete",
                    complete_result,
                    room=sid,
                )

                # 6. Log activity
                try:
                    await log_websocket_activity(
                        sid=sid,
                        event_key="documents.generated",
                        template="{{ actor.name }} generated document template",
                        context={
                            "department_id": department_id_str,
                        },
                        endpoint="/socket/v4/documents/generate",
                        error=False,
                    )
                except Exception:
                    pass

        except RuntimeError:
            # Pool not initialized - emit error event
            await emit_to_internal(
                "document_error",
                DocumentGenerationErrorApiRequest(
                    error_message="Database connection pool not available",
                    document_id=document_id_str,
                    group_id=str(group_id) if group_id else None,
                ),
                sid=sid,
            )
        except Exception as e:
            await emit_to_internal(
                "document_error",
                DocumentGenerationErrorApiRequest(
                    error_message=f"Failed to finalize document generation: {str(e)}",
                    document_id=document_id_str,
                    group_id=str(group_id) if group_id else None,
                ),
                sid=sid,
            )


@internal_sio.on("document_complete")  # type: ignore
async def document_complete_internal(
    data: dict[str, Any],
) -> None:
    """Handle document_complete event from internal bus."""
    # Note: Event payload structure doesn't match SQL function parameters
    # The handler extracts data and constructs SQL params separately
    sid = data.get("sid", "")
    if not sid:
        return

    from app.infra.v4.websocket.find_profile_by_socket import \
        find_profile_by_socket

    profile_id_str = await find_profile_by_socket(sid)
    if not profile_id_str:
        await emit_to_internal(
            "document_error",
            DocumentGenerationErrorApiRequest(
                error_message="Profile not found for socket",
                document_id=data.get("document_id"),
                group_id=data.get("group_id"),
            ),
            sid=sid,
        )
        return

    profile_id = uuid.UUID(profile_id_str)
    group_id = None
    if data.get("group_id"):
        try:
            group_id = uuid.UUID(data["group_id"])
        except (ValueError, TypeError):
            pass

    await _document_complete_impl(sid, data, profile_id, group_id)


register_server_endpoint(
    server_router,
    "/document_complete",
    dict[str, Any],  # type: ignore[arg-type]
    "Dispatch document complete to tool-specific handlers by tool_type",
)
