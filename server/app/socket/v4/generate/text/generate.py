SQL_PATH = "app/sql/v4/generate/text/get_text_run_context_and_create_run_complete.sql"

internal_sio = get_internal_sio()


async def _generate_text_impl(
    sid: str,
    data: GetTextRunContextAndCreateRunApiRequest,
    profile_id: uuid.UUID,
) -> None:
    """Handle generic text generation requests via WebSocket."""
    trace_id: str | None = None
    group_id: uuid.UUID | None = None

    try:
        async with get_db_connection() as conn:
            # Get all context data AND create run in single atomic transaction
            # This validates rate limits and creates run atomically
            try:
                # Use execute_sql_typed() - auto-detects function
                # Use double star pattern - SQL handles defaults via COALESCE in params CTE
                # Include group_id and user_instructions if present (for regeneration)
                params_dict = data.model_dump()
                # Add optional fields if they exist (for regeneration)
                if hasattr(data, "group_id") and data.group_id:
                    params_dict["group_id"] = data.group_id
                if hasattr(data, "user_instructions") and data.user_instructions is not None:
                    params_dict["user_instructions"] = data.user_instructions
                params = GetTextRunContextAndCreateRunSqlParams(
                    **params_dict,
                    profile_id=profile_id,  # From sid lookup
                )
                result = cast(
                    GetTextRunContextAndCreateRunSqlRow,
                    await execute_sql_typed(conn, SQL_PATH, params=params),
                )
            except Exception as e:
                import asyncpg  # type: ignore

                error_msg = str(e)
                # Check if it's a rate limit error from SQL (PostgreSQL exception)
                if (
                    isinstance(e, asyncpg.PostgresError)
                    and "RATE_LIMIT_EXCEEDED" in error_msg
                ):
                    # Extract the user-friendly message (everything after "RATE_LIMIT_EXCEEDED: ")
                    user_msg = (
                        error_msg.split("RATE_LIMIT_EXCEEDED: ", 1)[1]
                        if "RATE_LIMIT_EXCEEDED: " in error_msg
                        else error_msg
                    )
                    await emit_to_internal(
                        "generate_error",
                        {
                            "sid": sid,
                            "error_message": user_msg,
                            "resource_id": str(data.resource_id) if data.resource_id else None,
                            "group_id": None,
                            "resource_type": data.resource_type,
                        },
                        sid=sid,
                    )
                    return
                await emit_to_internal(
                    "generate_error",
                    {
                        "sid": sid,
                        "error_message": f"Failed to initialize text generation: {str(e)}",
                        "resource_id": str(data.resource_id) if data.resource_id else None,
                        "group_id": None,
                        "resource_type": data.resource_type,
                    },
                    sid=sid,
                )
                return

            if not result:
                await emit_to_internal(
                    "generate_error",
                    {
                        "sid": sid,
                        "error_message": "No agent configured for generation",
                        "resource_id": str(data.resource_id) if data.resource_id else None,
                        "group_id": None,
                        "resource_type": data.resource_type,
                    },
                    sid=sid,
                )
                return

            # result.group_id and result.trace_id come from groups table
            trace_id = result.trace_id  # From groups.trace_id
            group_id = result.group_id  # Created by SQL

            # Extract run_id from result (created in same transaction)
            model_run_id = uuid.UUID(result.run_id)

            # Emit start event via internal bus
            await internal_sio.emit(
                "generate_text_start",
                {
                    "sid": sid,
                    "resource_id": str(data.resource_id) if data.resource_id else None,
                    "resource_type": data.resource_type,
                    "run_id": str(model_run_id),
                    "message": f"Starting {result.agent_name or 'text'} generation",
                },
            )

            # Get tools from SQL result (composite type array)
            # Filter out tools with None names (shouldn't happen, but defensive)
            agent_tools_config = [
                tool for tool in (result.tools or []) if tool.name is not None
            ]

            # Build tools from database configs using generic helper
            text_tools: list[Tool] = []
            for tool in agent_tools_config:
                if tool.name is None:
                    continue
                try:
                    # Build tool config dict from composite type - build_tool_from_config expects dict
                    tool_config = {
                        "id": str(tool.id),
                        "name": tool.name,
                        "description": tool.description or "",
                        "tool_type": tool.tool_type or "",
                        "agent_role": tool.agent_role or "",
                        "arguments": tool.arguments,
                        "argument_descriptions": tool.argument_descriptions,
                        "argument_defaults": tool.argument_defaults,
                        "active": tool.active,
                    }
                    built_tool = build_tool_from_config(tool_config)
                    text_tools.append(built_tool)
                except Exception as e:
                    # Log error but continue - don't break if one tool fails
                    import logging

                    logging.getLogger(__name__).warning(
                        f"Failed to build tool {tool.name}: {e}"
                    )

            # Create tool use behavior
            def tool_use_behavior(
                tool_context: RunContextWrapper[Any],
                tool_results: list[FunctionToolResult],
            ) -> ToolsToFinalOutputResult:
                # Tool completion is checked after streaming completes by querying SQL
                # For now, always return False to let agent continue calling tools
                return ToolsToFinalOutputResult(is_final_output=False)

            # Build text agent inline - use SQL result fields directly
            text_agent = GenericAgent(
                agent_name=result.agent_name or "",
                system_prompt=result.system_prompt or "",
                temperature=result.temperature or 0.0,
                model_name=result.model_name or "",
                provider=result.provider or "",
                base_url=result.base_url,
                api_key=result.api_key or "",
                reasoning=result.reasoning,
                tools=text_tools,
                parallel_tool_calls=False,
                tool_use_behavior=tool_use_behavior,
            )

            # Construct input items for the agent
            # For generic text generation, we accept input_items from the request
            # If not provided, use empty list (agent will use system prompt only)
            input_items: list[TResponseInputItem] = []
            
            # Handle audio input if upload_id is provided (for audio agent)
            if data.upload_id and result.upload_id and result.file_path:
                audio_file_path = UPLOAD_FOLDER / result.file_path
                if audio_file_path.exists():
                    # Add audio file to input items
                    # For OpenAI audio models, we can add audio input items
                    # For now, include file path in content (agents library will handle it)
                    audio_input: TResponseInputItem = {
                        "role": "user",
                        "content": f"Audio file to process: {audio_file_path}",
                    }
                    input_items.append(audio_input)
            
            # Get developer instruction template from SQL result
            developer_message_content: str | None = (
                result.developer_instruction_template
            )
            if developer_message_content:
                # Render Jinja template with user_instructions if provided (for regeneration)
                template = Template(developer_message_content)
                user_instructions = getattr(data, "user_instructions", None)
                developer_message_content = template.render(
                    user_instructions=user_instructions if user_instructions else ""
                )

                developer_message: TResponseInputItem = {
                    "role": "developer",
                    "content": developer_message_content,
                }
                input_items.append(developer_message)
                # Developer message linking is handled in SQL (result.developer_message_id)

            # Track completed tool names for verification (SQL handles persistence)
            # Get required tool names from database result
            required_tool_names: set[str] = {
                tool.name for tool in agent_tools_config if tool.name is not None
            }
            completed_tool_names: set[str] = set()
            tool_call_id_to_name: dict[str, str] = {}
            tool_call_id_to_call_id: dict[str, str | None] = {}  # Store call_id mapping
            # Map tool_name to tool_type from database result
            tool_name_to_type: dict[str, str] = {
                tool.name: tool.tool_type
                for tool in agent_tools_config
                if tool.name is not None and tool.tool_type is not None
            }

            # Run text generation with streaming
            resource_id_str = str(data.resource_id) if data.resource_id else sid
            with trace(
                f"{result.agent_name or 'Text'} Agent",
                trace_id=trace_id,  # From groups table
                group_id=resource_id_str,  # Resource ID, not database group_id
            ):
                result_runner = Runner.run_streamed(
                    text_agent.agent(),
                    input_items,
                    context=DebugContext(conn=conn, run_id=model_run_id),
                )

            # Store the result in active runs for potential cancellation
            from app.infra.v4.websocket.store_active_run import \
                store_active_run

            await store_active_run(resource_id_str, result_runner)

            try:
                # Use generic streaming helper instead of manual parsing
                async def on_start(
                    tool_call_id: str, tool_name: str, call_id: str | None
                ) -> None:
                    tool_call_id_to_name[tool_call_id] = tool_name
                    tool_call_id_to_call_id[tool_call_id] = call_id
                    await internal_sio.emit(
                        "generate_text_progress",
                        {
                            "sid": sid,
                            "progress_type": "tool_call_start",
                            "resource_id": str(data.resource_id) if data.resource_id else None,
                            "resource_type": data.resource_type,  # Include for routing
                            "run_id": str(model_run_id),
                            "tool_call_id": tool_call_id,
                            "call_id": call_id or tool_call_id or "",
                            "tool_name": tool_name or "",
                            "arguments_delta": "",  # Empty for start
                        },
                    )

                async def on_progress(tool_call_id: str, arguments_delta: str) -> None:
                    # For progress events, call_id and tool_name may be None - SQL will look them up
                    await internal_sio.emit(
                        "generate_text_progress",
                        {
                            "sid": sid,
                            "progress_type": "tool_call_progress",
                            "resource_id": str(data.resource_id) if data.resource_id else None,
                            "resource_type": data.resource_type,  # Include for routing
                            "run_id": str(model_run_id),
                            "tool_call_id": tool_call_id,
                            "call_id": None,  # SQL will look it up
                            "tool_name": None,  # SQL will look it up
                            "arguments_delta": arguments_delta,  # Delta - SQL accumulates
                        },
                    )

                async def on_complete(
                    tool_call_id: str, final_args: dict[str, Any]
                ) -> None:
                    tool_name = tool_call_id_to_name.get(tool_call_id, "")
                    if tool_name:
                        completed_tool_names.add(tool_name)

                    # Get call_id from stored mapping
                    call_id = tool_call_id_to_call_id.get(tool_call_id)

                    # Map tool_name to tool_type from database result
                    tool_type: str | None = (
                        tool_name_to_type.get(tool_name) if tool_name else None
                    )

                    await internal_sio.emit(
                        "generate_text_complete",
                        {
                            "sid": sid,
                            "type": "tool_call_complete",
                            "resource_id": str(data.resource_id) if data.resource_id else None,
                            "resource_type": data.resource_type,  # Include for routing
                            "run_id": str(model_run_id),
                            "group_id": str(group_id) if group_id else None,
                            "tool_call_id": tool_call_id,
                            "call_id": call_id or tool_call_id or "",
                            "tool_name": tool_name or "",
                            "tool_type": tool_type,
                            "final_content": str(final_args),
                            "arguments_raw": "",  # Will be retrieved from SQL if needed
                        },
                    )

                callbacks = StreamEventCallbacks(
                    on_tool_call_start=on_start,
                    on_tool_call_progress=on_progress,
                    on_tool_call_complete=on_complete,
                )

                # Generate tool_call_id from call_id
                def tool_call_id_generator(call_id: str | None) -> str:
                    if call_id:
                        return call_id
                    return f"text_{uuid.uuid4().hex[:16]}"

                await stream_agent_events(
                    result_runner, callbacks, tool_call_id_generator
                )

            except BaseException as stream_error:
                if isinstance(
                    stream_error,
                    (asyncio.CancelledError, KeyboardInterrupt, SystemExit),
                ):
                    raise
                raise
            except Exception:
                raise
            finally:
                # Clean up active run
                from app.infra.v4.websocket.remove_active_run import \
                    remove_active_run

                await remove_active_run(resource_id_str)

            # Verify all required tools were called (based on database config)
            missing_tools = required_tool_names - completed_tool_names
            if missing_tools:
                tool_names_str = ", ".join(sorted(missing_tools))
                await emit_to_internal(
                    "generate_error",
                    {
                        "sid": sid,
                        "error_message": (
                            f"Agent did not call all required tools. "
                            f"Missing: {tool_names_str}"
                        ),
                        "resource_id": str(data.resource_id) if data.resource_id else None,
                        "group_id": str(group_id) if group_id else None,
                        "resource_type": data.resource_type,
                    },
                    sid=sid,
                )
                return

            # Emit run completion event with usage data - generate/end.py will handle log_run
            usage = result_runner.context_wrapper.usage
            assistant_output = getattr(result_runner, "final_output", None) or ""
            await internal_sio.emit(
                "generate_text_complete",
                {
                    "sid": sid,
                    "type": "run_complete",
                    "resource_id": str(data.resource_id) if data.resource_id else None,
                    "resource_type": data.resource_type,
                    "run_id": str(model_run_id),
                    "group_id": str(group_id) if group_id else None,
                    "department_id": str(data.department_id) if data.department_id else None,
                    # Include usage data for log_run in generate/end.py
                    "input_text_tokens": usage.input_tokens,
                    "output_text_tokens": usage.output_tokens,
                    "system_prompt": result.system_prompt,
                    "input_items": input_items,  # Serialized TResponseInputItem list
                    "assistant_output": assistant_output,
                },
            )

    except RuntimeError:
        # Pool not initialized - emit error event
        resource_id_err: uuid.UUID | None = None
        resource_type_err: str | None = None
        try:
            resource_id_err = data.resource_id  # Already UUID
            resource_type_err = data.resource_type
        except Exception:
            pass
        await internal_sio.emit(
            "generate_error",
            {
                "sid": sid,
                "error_message": "Database connection pool not available",
                "resource_id": str(resource_id_err) if resource_id_err else None,
                "group_id": None,
                "resource_type": resource_type_err,
            },
        )
    except Exception as e:
        # Extract resource_id safely for error message
        resource_id_err2: uuid.UUID | None = None
        resource_type_err2: str | None = None
        try:
            resource_id_err2 = data.resource_id  # Already UUID
            resource_type_err2 = data.resource_type
        except Exception:
            pass
        await internal_sio.emit(
            "generate_error",
            {
                "sid": sid,
                "error_message": str(e),
                "resource_id": str(resource_id_err2) if resource_id_err2 else None,
                "group_id": None,
                "resource_type": resource_type_err2,
            },
        )
        # Log activity error
        try:
            await log_websocket_activity(
                sid=sid,
                event_key="text.generated",
                template="{{ actor.name }} failed to generate text",
                context={"error": str(e)},
                endpoint="/socket/v4/generate/text",
                error=True,
            )
        except Exception:
            pass


from app.main import sio


@sio.event  # type: ignore
async def generate_text(sid: str, data: dict[str, Any]) -> None:
    """Wrapper that validates payload before calling actual handler"""
    # Server always expects snake_case - frontend must convert camelCase before sending
    await handle_client_event(
        sid=sid,
        data=data,
        request_type=GetTextRunContextAndCreateRunApiRequest,
        handler=_generate_text_impl,  # type: ignore[arg-type]
        error_event_name="text_generation_error",
        error_response_type=None,  # Error handler uses TextGenerationErrorApiRequest (defined in error.py)
    )


@internal_sio.on("generate_text")  # type: ignore
async def generate_text_internal(data: dict[str, Any]) -> None:
    """Handle generate_text event from internal bus (server-to-server)."""
    try:
        # Extract sid first
        sid = data.get("sid", "")
        if not sid:
            return
        
        # Create payload from known fields (ApiRequest might not have group_id/user_instructions yet)
        payload_dict = {
            k: v for k, v in data.items() 
            if k not in ("sid", "group_id", "user_instructions", "trace_id")
        }
        payload = GetTextRunContextAndCreateRunApiRequest(**payload_dict)
        
        # Get profile_id from sid lookup for internal events
        from app.infra.v4.websocket.find_profile_by_socket import \
            find_profile_by_socket
        
        profile_id_str = await find_profile_by_socket(sid)
        if not profile_id_str:
            await emit_to_internal(
                "generate_error",
                {
                    "sid": sid,
                    "error_message": "Profile not found. Please reconnect.",
                    "resource_id": str(payload.resource_id) if payload.resource_id else None,
                    "group_id": None,
                    "resource_type": payload.resource_type,
                },
                sid=sid,
            )
            return
        profile_id = uuid.UUID(profile_id_str)
        
        # Attach additional fields to payload object for passing to SQL
        payload.group_id = uuid.UUID(data["group_id"]) if data.get("group_id") else None
        payload.user_instructions = data.get("user_instructions")
        
        await _generate_text_impl(sid, payload, profile_id)
    except Exception as e:
        resource_id = data.get("resource_id")
        resource_type = data.get("resource_type")
        sid = data.get("sid", "")
        if sid:
            await emit_to_internal(
                "generate_error",
                {
                    "sid": sid,
                    "error_message": f"Invalid request: {str(e)}",
                    "resource_id": str(resource_id) if resource_id else None,
                    "group_id": None,
                    "resource_type": resource_type,
                },
                sid=sid,
            )


register_client_endpoint(
    client_router,
    "/generate_text",
    GetTextRunContextAndCreateRunApiRequest,
    "Generate text using AI agent",
)

