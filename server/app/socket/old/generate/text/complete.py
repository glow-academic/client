async def _generate_text_complete_impl(
    sid: str,
    data: dict[str, Any],  # Event payload - will be validated by handler wrapper
    profile_id: uuid.UUID,
    group_id: uuid.UUID | None = None,
) -> None:
    """Route completion events - tool_call_complete to agent tools, run_complete to generate/end.py."""
    event_type = data.get("type") if isinstance(data, dict) else getattr(data, "type", None)
    
    if event_type == "tool_call_complete":
        # Tool-specific completion - route to agent-specific tool handler
        tool_name = data.get("tool_name") if isinstance(data, dict) else getattr(data, "tool_name", None)
        resource_type = data.get("resource_type") if isinstance(data, dict) else getattr(data, "resource_type", None)
        
        if tool_name and resource_type:
            # Route to agent-specific tool handler
            tool_event_name = f"{resource_type}_{tool_name}_complete"
            await internal_sio.emit(
                tool_event_name,
                {
                    "sid": sid,
                    "resource_id": data.get("resource_id"),
                    "run_id": data.get("run_id"),
                    "tool_call_id": data.get("tool_call_id"),
                    "call_id": data.get("call_id"),
                    "tool_name": tool_name,
                    "final_content": data.get("final_content"),
                    "arguments_raw": data.get("arguments_raw"),
                },
            )
        else:
            # Fallback: emit generic client event
            from app.main import sio as client_sio
            await client_sio.emit(
                "text_tool_call_complete",
                {
                    "success": True,
                    "resource_id": data.get("resource_id"),
                    "run_id": data.get("run_id"),
                    "tool_call_id": getattr(data, "tool_call_id", "") or "",
                    "tool_type": getattr(data, "tool_type", None),
                },
                room=sid,
            )

    elif event_type == "run_complete":
        # Route to generate/end.py for centralized handling (log_run + agent end dispatch)
        from app.socket.v4.generate.end import (GenerateEndApiRequest,
                                                _generate_end_impl)

        # Transform to GenerateEndApiRequest format
        payload = GenerateEndApiRequest(
            sid=sid,
            type="run_complete",
            resource_id=data.get("resource_id"),
            resource_type=data.get("resource_type"),
            run_id=data.get("run_id", ""),
            group_id=data.get("group_id"),
            department_id=data.get("department_id"),
            # Include usage data for log_run
            input_text_tokens=data.get("input_text_tokens"),
            output_text_tokens=data.get("output_text_tokens"),
            system_prompt=data.get("system_prompt"),
            input_items=data.get("input_items"),
            assistant_output=data.get("assistant_output"),
        )
        
        await _generate_end_impl(sid, payload, profile_id)
        return


@internal_sio.on("generate_text_complete")  # type: ignore
async def generate_text_complete_internal(
    data: dict[str, Any],
) -> None:
    """Handle generate_text_complete event from internal bus."""
    # Note: Event payload structure doesn't match SQL function parameters
    # The handler extracts data and constructs SQL params separately
    sid = data.get("sid", "")
    if not sid:
        return

    from app.infra.v4.websocket.find_profile_by_socket import \
        find_profile_by_socket

    profile_id_str = await find_profile_by_socket(sid)
    if not profile_id_str:
        await emit_to_internal(
            "generate_error",
            GenerateErrorApiRequest(
                sid=sid,
                error_message="Profile not found for socket",
                resource_id=data.get("resource_id"),
                group_id=data.get("group_id"),
                resource_type=data.get("resource_type"),
            ),
            sid=sid,
        )
        return

    profile_id = uuid.UUID(profile_id_str)
    group_id = None
    if data.get("group_id"):
        try:
            group_id = uuid.UUID(data["group_id"])
        except (ValueError, TypeError):
            pass

    await _generate_text_complete_impl(sid, data, profile_id, group_id)


