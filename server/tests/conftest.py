# --- CORE TEST FIXTURES ---


@pytest.fixture(scope="session")
def pg_container():
    """Start PostgreSQL container for test session."""
    with PostgresContainer("postgres:16") as pg:
        yield pg


@pytest.fixture(scope="session")
def schema_sql() -> str:
    """Load consolidated test schema SQL file.
    
    The schema is pre-generated by running: make generate-test-schema
    This includes full schema + CS seed data (agents, models, personas, etc.)
    """
    schema_file = Path(__file__).parent / "test-schema.sql"
    
    if not schema_file.exists():
        raise FileNotFoundError(
            f"Test schema file not found: {schema_file}\n"
            "Please run 'make generate-test-schema' to generate it."
        )
    
    return schema_file.read_text()


@pytest.fixture(scope="session")
def db_initialized(pg_container: PostgresContainer, schema_sql: str) -> str:
    """Apply schema once at session start, return connection string."""
    import asyncio
    
    conn_uri = pg_container.get_connection_url()
    # Fix URL scheme for asyncpg (testcontainers returns postgresql+psycopg2)
    conn_uri = conn_uri.replace("postgresql+psycopg2://", "postgresql://")
    
    async def _init_db():
        conn = await asyncpg.connect(dsn=conn_uri)
        try:
            await conn.execute(schema_sql)
        finally:
            await conn.close()
    
    # Run initialization
    asyncio.run(_init_db())
    
    return conn_uri


@pytest_asyncio.fixture
async def db(db_initialized: str) -> AsyncGenerator[asyncpg.Connection, None]:
    """Provide clean database connection with transaction rollback.
    
    Each test gets:
    - Connection to database with schema already applied (session level)
    - Transaction that rolls back after test completes (test isolation)
    """
    # Connect to initialized database
    conn = await asyncpg.connect(dsn=db_initialized)
    
    # Start transaction for test isolation
    tx = conn.transaction()
    await tx.start()
    
    try:
        yield conn
    finally:
        await tx.rollback()  # Undo all test changes
        await conn.close()


