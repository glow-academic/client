#!/usr/bin/env node
/**
 * Generate api/index.ts barrel file from versioned .ts files.
 *
 * Usage:
 *   node scripts/generate-api-index.cjs
 */

const fs = require('fs');
const path = require('path');

const dir = 'api';
const files = fs.readdirSync(dir)
  .filter(f => /^v\d+\.\d+\.\d+\.ts$/.test(f))
  .sort((a, b) => {
    const pa = a.replace('.ts', '').replace('v', '').split('.').map(Number);
    const pb = b.replace('.ts', '').replace('v', '').split('.').map(Number);
    for (let i = 0; i < 3; i++) { if (pa[i] !== pb[i]) return pa[i] - pb[i]; }
    return 0;
  });

if (files.length === 0) {
  console.log('No version files found in api/');
  process.exit(0);
}

const versions = files.map(f => f.replace('.ts', ''));
const latest = versions[versions.length - 1];

let out = '// Auto-generated by generate-api-index.cjs — do not edit\n\n';

for (const v of versions) {
  const suffix = v.replace(/[^a-zA-Z0-9]/g, '_');
  out += `export type { paths as Paths_${suffix}, components as Components_${suffix} } from './${v}'\n`;
}

out += '\n// Latest\n';
out += `export type { paths, components } from './${latest}'\n`;
out += `export const LATEST_API_VERSION = '${latest}' as const\n`;
out += `export const SUPPORTED_VERSIONS = [${versions.map(v => `'${v}'`).join(', ')}] as const\n`;
out += `export type SupportedVersion = typeof SUPPORTED_VERSIONS[number]\n`;

fs.writeFileSync(path.join(dir, 'index.ts'), out);
console.log(`Generated api/index.ts (${versions.length} versions, latest: ${latest})`);
