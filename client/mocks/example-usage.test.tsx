import { render, screen, waitFor } from "@/test/custom-render";
// Import the mockDb instance exported from your setup file!
import { createMockAgent, createMockCohort } from "@/mocks/factories";
import { mockDb } from "@/test/setup";
import { describe, expect, it } from "vitest";

// Example component that uses server actions
const ExampleComponent = () => {
  // This would normally use server actions, but they're now mocked
  return (
    <div>
      <h1>Example Component</h1>
      <p>This component would normally fetch data from server actions</p>
    </div>
  );
};

describe("Type-Safe Mock System Examples", () => {
  it("should display the default list of agents", async () => {
    render(<ExampleComponent />);

    // The default data from mocks/schema.ts is automatically available
    // through the mockDb instance that's set up in test/setup.ts
    await waitFor(() => {
      expect(screen.getByText("Example Component")).toBeInTheDocument();
    });
  });

  it("should display a custom agent created for this specific test", async () => {
    // ARRANGE: Customize the mock database before rendering
    const customAgent = createMockAgent({
      name: "Special History Agent",
      description: "A specialized agent for history questions",
    });
    mockDb.agents.push(customAgent);

    // ACT
    render(<ExampleComponent />);

    // ASSERT
    await waitFor(() => {
      expect(screen.getByText("Example Component")).toBeInTheDocument();
    });
  });

  it("should demonstrate dynamic mock database operations", async () => {
    // ARRANGE: Create custom data using the factories
    const customCohort = createMockCohort({
      title: "Test Cohort 2024",
      active: true,
      description: "A test cohort for demonstration",
    });

    // Add it to the mock database
    mockDb.cohorts.push(customCohort);

    // Use the dynamic methods
    const foundCohort = mockDb.getCohort(customCohort.id);
    expect(foundCohort).toEqual(customCohort);

    // Update the cohort
    const updatedCohort = mockDb.updateCohort(customCohort.id, {
      title: "Updated Test Cohort 2024",
    });
    expect(updatedCohort?.title).toBe("Updated Test Cohort 2024");

    // Create a new agent using the factory
    const newAgent = mockDb.createAgent({
      name: "Dynamic Test Agent",
      systemPrompt: "You are a test agent",
    });
    expect(newAgent.name).toBe("Dynamic Test Agent");
    expect(mockDb.agents).toContain(newAgent);
  });

  it("should demonstrate type safety with factories", async () => {
    // The factories provide full type safety
    const agent = createMockAgent({
      name: "Type Safe Agent",
      // TypeScript will catch errors here:
      // invalidField: "this would cause a type error"
    });

    expect(agent.name).toBe("Type Safe Agent");
    expect(agent.id).toBeDefined(); // Generated by faker
    expect(agent.createdAt).toBeDefined(); // Generated by faker
  });

  it("should demonstrate test isolation", async () => {
    // Each test gets a fresh mockDb instance
    const initialAgentCount = mockDb.agents.length;

    // Add an agent
    mockDb.createAgent({ name: "Isolation Test Agent" });

    // Verify it was added
    expect(mockDb.agents.length).toBe(initialAgentCount + 1);

    // In the next test, mockDb will be fresh again
  });
});
