#!/usr/bin/env python3
"""
Split database/schema.sql (pg_dump output) into structured files under database/schema/.

Parses pg_dump section markers (-- Name: ...; Type: ...; Schema: ...) and routes
each section to the appropriate output file. Skips functions, composite types
(types schema), and materialized views (hand-maintained in database/schema/views/).

Output structure:
    database/schema/
      extensions.sql
      enums/
        <enum_name>.sql          — one file per enum
      tables/
        artifacts/<domain>.sql   — CREATE TABLE + inline PK/unique/check constraints
        entries/<domain>.sql
        resources/<domain>.sql
        junctions/<domain>.sql
        connections/<domain>.sql
      indexes/
        artifacts/<domain>.sql   — CREATE INDEX (parallel to tables/)
        entries/<domain>.sql
        ...
      foreign_keys/
        artifacts/<domain>.sql   — ALTER TABLE ... ADD CONSTRAINT ... FK
        entries/<domain>.sql
        ...
      views/                     — NOT generated here (hand-maintained MV definitions)

Usage:
    python database/scripts/split_schema.py [path/to/schema.sql]
"""

import os
import re
import shutil
import sys
from collections import defaultdict

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------

SECTION_RE = re.compile(
    r"^-- Name: (?P<name>.+?); Type: (?P<type>.+?); Schema: (?P<schema>.+?); Owner: .+$"
)

META_CMD_RE = re.compile(r"^\\(restrict|unrestrict)\b")

DUMP_COMMENT_RE = re.compile(
    r"^-- (PostgreSQL database dump|Dumped from|Dumped by|PostgreSQL database dump complete)"
)

AUTO_GEN_HEADER = "-- Auto-generated by split_schema.py — do not edit manually"

# Table suffix → subfolder name
TABLE_SUFFIXES = {
    "_artifact": "artifacts",
    "_entry": "entries",
    "_resource": "resources",
    "_junction": "junctions",
    "_connection": "connections",
}


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def get_table_suffix_and_domain(table_name: str) -> tuple[str, str]:
    """Extract (suffix_folder, domain_prefix) from a table name."""
    for suffix, folder in TABLE_SUFFIXES.items():
        if table_name.endswith(suffix):
            prefix = table_name[: -len(suffix)]
            domain = prefix.split("_")[0] if prefix else table_name
            return folder, domain
    return "other", table_name.split("_")[0]


def extract_table_from_index_body(body_lines: list[str]) -> str | None:
    """Extract table name from CREATE INDEX ... ON table_name."""
    for line in body_lines:
        s = line.strip()
        if s.upper().startswith("CREATE") and "INDEX" in s.upper():
            m = re.search(r"\bON\s+(?:ONLY\s+)?(?:public\.)?(\w+)", s, re.IGNORECASE)
            if m:
                return m.group(1)
    return None


def extract_table_from_constraint_name(name: str) -> str:
    """pg_dump CONSTRAINT name format: 'table_name constraint_name'."""
    return name.split(" ")[0]


def is_mv_table(table_name: str) -> bool:
    """Check if a table name is a materialized view."""
    return table_name.endswith("_mv")


# ---------------------------------------------------------------------------
# Section types
# ---------------------------------------------------------------------------

def classify_section(sec_type: str, schema: str) -> str | None:
    """Return a broad category or None to skip."""
    t = sec_type.strip()
    s = schema.strip()

    if t in ("SCHEMA", "EXTENSION"):
        return "extensions"
    if t == "COMMENT" and s == "-":
        return "extensions"
    if t == "COMMENT" and s == "public":
        return "table_comment"
    if t == "TYPE" and s == "public":
        return "enums"
    if t == "TYPE":
        return None  # skip types schema composites
    if t == "TABLE" and s == "public":
        return "tables"
    if t == "CONSTRAINT" and s == "public":
        return "constraints"
    if t == "INDEX" and s == "public":
        return "indexes"
    if t == "FK CONSTRAINT" and s == "public":
        return "foreign_keys"
    # Skip FUNCTION, MATERIALIZED VIEW, etc.
    return None


# ---------------------------------------------------------------------------
# Parsing
# ---------------------------------------------------------------------------

class Section:
    __slots__ = ("name", "sec_type", "schema", "header_lines", "body_lines")

    def __init__(self, name: str, sec_type: str, schema: str):
        self.name = name
        self.sec_type = sec_type
        self.schema = schema
        self.header_lines: list[str] = []
        self.body_lines: list[str] = []

    def to_text(self) -> str:
        return "\n".join(self.header_lines + self.body_lines)


def parse_sections(schema_path: str) -> tuple[list[str], list[Section]]:
    """Parse schema.sql into preamble lines and a list of sections."""
    preamble: list[str] = []
    sections: list[Section] = []
    current: Section | None = None
    in_preamble = True

    with open(schema_path, "r") as f:
        for raw_line in f:
            line = raw_line.rstrip("\n")

            if META_CMD_RE.match(line):
                continue
            if DUMP_COMMENT_RE.match(line):
                continue

            m = SECTION_RE.match(line)
            if m:
                in_preamble = False
                current = Section(m.group("name"), m.group("type"), m.group("schema"))
                current.header_lines.append(line)
                sections.append(current)
                continue

            if in_preamble:
                if line.strip() == "--":
                    continue
                preamble.append(line)
            elif current is not None:
                if not current.body_lines:
                    if line.strip() in ("--", ""):
                        current.header_lines.append(line)
                        continue
                current.body_lines.append(line)

    return preamble, sections


# ---------------------------------------------------------------------------
# Writing
# ---------------------------------------------------------------------------

def write_file(path: str, header: str, content: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(f"-- {header}\n")
        f.write(f"{AUTO_GEN_HEADER}\n")
        f.write("--\n\n")
        f.write(content)
        if not content.endswith("\n"):
            f.write("\n")


# ---------------------------------------------------------------------------
# Main split logic
# ---------------------------------------------------------------------------

def write_split_files(schema_path: str, output_dir: str) -> dict:
    """Parse schema.sql, classify sections, write structured files."""

    # Clean output dir (preserve hand-maintained files)
    PRESERVE = {"views", "functions.sql"}
    if os.path.exists(output_dir):
        for item in os.listdir(output_dir):
            if item in PRESERVE:
                continue
            item_path = os.path.join(output_dir, item)
            if os.path.isdir(item_path):
                shutil.rmtree(item_path)
            else:
                os.remove(item_path)
    os.makedirs(output_dir, exist_ok=True)

    preamble, sections = parse_sections(schema_path)

    # --- Collect into buckets ---

    extensions_parts: list[str] = []
    enum_parts: dict[str, list[str]] = {}
    # tables_parts[suffix_folder][domain] = [section texts]
    # constraints get inlined here too
    tables_parts: dict[str, dict[str, list[str]]] = defaultdict(lambda: defaultdict(list))
    # indexes_parts[suffix_folder][domain] = [section texts]  (parallel to tables)
    indexes_parts: dict[str, dict[str, list[str]]] = defaultdict(lambda: defaultdict(list))
    # foreign_keys_parts[suffix_folder][domain] = [section texts]
    fk_parts: dict[str, dict[str, list[str]]] = defaultdict(lambda: defaultdict(list))

    orphan_sets: list[str] = []
    counts: dict[str, int] = defaultdict(int)

    # Preamble → extensions
    preamble_stripped = list(preamble)
    while preamble_stripped and preamble_stripped[0].strip() == "":
        preamble_stripped.pop(0)
    while preamble_stripped and preamble_stripped[-1].strip() == "":
        preamble_stripped.pop()
    extensions_parts.append("\n".join(preamble_stripped))

    for sec in sections:
        key = classify_section(sec.sec_type, sec.schema)

        if key is None:
            # Collect orphan SET statements from skipped sections.
            # Only top-level pg_dump SET statements (no indentation, specific patterns).
            for line in sec.body_lines:
                stripped = line.strip()
                if (
                    line == stripped  # no leading whitespace = top-level
                    and stripped.upper().startswith("SET ")
                    and any(
                        stripped.upper().startswith(f"SET {kw}")
                        for kw in (
                            "DEFAULT_TABLESPACE",
                            "DEFAULT_TABLE_ACCESS_METHOD",
                            "STATEMENT_TIMEOUT",
                            "LOCK_TIMEOUT",
                            "CLIENT_ENCODING",
                            "STANDARD_CONFORMING_STRINGS",
                            "CHECK_FUNCTION_BODIES",
                            "XMLOPTION",
                            "CLIENT_MIN_MESSAGES",
                            "ROW_SECURITY",
                            "IDLE_IN_TRANSACTION_SESSION_TIMEOUT",
                            "TRANSACTION_TIMEOUT",
                            "SEARCH_PATH",
                        )
                    )
                ):
                    orphan_sets.append(line)
            continue

        text = sec.to_text()

        # Prepend orphan SETs if accumulated
        orphan_prefix = ""
        if orphan_sets:
            orphan_prefix = "\n".join(orphan_sets) + "\n\n"
            orphan_sets = []

        if key == "extensions":
            extensions_parts.append(orphan_prefix + text)
            counts["extensions"] += 1

        elif key == "enums":
            enum_parts[sec.name] = [orphan_prefix + text]
            counts["enums"] += 1

        elif key == "tables":
            suffix_folder, domain = get_table_suffix_and_domain(sec.name)
            if orphan_prefix:
                tables_parts[suffix_folder][domain].append(orphan_prefix.rstrip())
            tables_parts[suffix_folder][domain].append(text)
            counts["tables"] += 1

        elif key == "table_comment":
            # COMMENT ON TABLE — inline with table
            table_name = sec.name.replace("TABLE ", "")
            suffix_folder, domain = get_table_suffix_and_domain(table_name)
            tables_parts[suffix_folder][domain].append(text)
            counts["table_comments"] += 1

        elif key == "constraints":
            # Inline with table file
            table_name = extract_table_from_constraint_name(sec.name)
            suffix_folder, domain = get_table_suffix_and_domain(table_name)
            tables_parts[suffix_folder][domain].append(text)
            counts["constraints"] += 1

        elif key == "indexes":
            table_name = extract_table_from_index_body(sec.body_lines)
            if table_name and is_mv_table(table_name):
                counts["mv_indexes_skipped"] += 1
                continue  # MV indexes handled by views
            if table_name:
                suffix_folder, domain = get_table_suffix_and_domain(table_name)
            else:
                suffix_folder, domain = "other", "unknown"
            if orphan_prefix:
                indexes_parts[suffix_folder][domain].append(orphan_prefix.rstrip())
            indexes_parts[suffix_folder][domain].append(text)
            counts["indexes"] += 1

        elif key == "foreign_keys":
            table_name = extract_table_from_constraint_name(sec.name)
            suffix_folder, domain = get_table_suffix_and_domain(table_name)
            if orphan_prefix:
                fk_parts[suffix_folder][domain].append(orphan_prefix.rstrip())
            fk_parts[suffix_folder][domain].append(text)
            counts["foreign_keys"] += 1

    # Drain remaining orphans
    if orphan_sets:
        extensions_parts.append("\n".join(orphan_sets))

    # --- Write files ---

    # extensions.sql
    write_file(
        os.path.join(output_dir, "extensions.sql"),
        "Extensions, schemas, and session settings",
        "\n\n".join(extensions_parts) + "\n",
    )

    # enums/
    for enum_name in sorted(enum_parts):
        write_file(
            os.path.join(output_dir, "enums", f"{enum_name}.sql"),
            f"Enum: {enum_name}",
            "\n\n".join(enum_parts[enum_name]) + "\n",
        )

    # tables/ — suffix subfolders with domain files (includes inline constraints)
    file_counts: dict[str, dict[str, int]] = {"tables": {}, "indexes": {}, "foreign_keys": {}}
    for suffix_folder in sorted(tables_parts):
        domains = tables_parts[suffix_folder]
        file_counts["tables"][suffix_folder] = len(domains)
        for domain in sorted(domains):
            write_file(
                os.path.join(output_dir, "tables", suffix_folder, f"{domain}.sql"),
                f"Table definitions + constraints: {domain}_*",
                "\n\n".join(domains[domain]) + "\n",
            )

    # indexes/ — parallel structure to tables/
    for suffix_folder in sorted(indexes_parts):
        domains = indexes_parts[suffix_folder]
        file_counts["indexes"][suffix_folder] = len(domains)
        for domain in sorted(domains):
            write_file(
                os.path.join(output_dir, "indexes", suffix_folder, f"{domain}.sql"),
                f"Indexes: {domain}_*",
                "\n\n".join(domains[domain]) + "\n",
            )

    # foreign_keys/ — parallel structure to tables/
    for suffix_folder in sorted(fk_parts):
        domains = fk_parts[suffix_folder]
        file_counts["foreign_keys"][suffix_folder] = len(domains)
        for domain in sorted(domains):
            write_file(
                os.path.join(output_dir, "foreign_keys", suffix_folder, f"{domain}.sql"),
                f"Foreign keys: {domain}_*",
                "\n\n".join(domains[domain]) + "\n",
            )

    return dict(counts), file_counts


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    db_dir = os.path.dirname(script_dir)

    schema_path = sys.argv[1] if len(sys.argv) > 1 else os.path.join(db_dir, "schema.sql")
    output_dir = os.path.join(db_dir, "schema")

    if not os.path.exists(schema_path):
        print(f"Error: {schema_path} not found", file=sys.stderr)
        sys.exit(1)

    print(f"Splitting {schema_path} → {output_dir}/")
    counts, file_counts = write_split_files(schema_path, output_dir)

    print(f"\nRouted sections:")
    print(f"  extensions.sql              {counts.get('extensions', 0):5d} sections")
    print(f"  enums/                      {counts.get('enums', 0):5d} files")

    total_tables = counts.get('tables', 0)
    total_constraints = counts.get('constraints', 0)
    total_comments = counts.get('table_comments', 0)
    print(f"  tables/                     {total_tables:5d} tables + {total_constraints} constraints + {total_comments} comments (inlined)")
    for folder, n in sorted(file_counts.get("tables", {}).items()):
        print(f"    {folder + '/':26s}{n:3d} domain files")

    print(f"  indexes/                    {counts.get('indexes', 0):5d} indexes ({counts.get('mv_indexes_skipped', 0)} MV indexes skipped)")
    for folder, n in sorted(file_counts.get("indexes", {}).items()):
        print(f"    {folder + '/':26s}{n:3d} domain files")

    print(f"  foreign_keys/               {counts.get('foreign_keys', 0):5d} FK constraints")
    for folder, n in sorted(file_counts.get("foreign_keys", {}).items()):
        print(f"    {folder + '/':26s}{n:3d} domain files")

    views_dir = os.path.join(output_dir, "views")
    if os.path.exists(views_dir):
        view_count = sum(1 for _ in _rglob_sql(views_dir))
        print(f"  views/                      {view_count:5d} MV files (hand-maintained)")


def _rglob_sql(d: str):
    for root, _, files in os.walk(d):
        for f in files:
            if f.endswith(".sql"):
                yield os.path.join(root, f)


if __name__ == "__main__":
    main()
