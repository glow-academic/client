#!/usr/bin/env python3
"""
Split database/schema.sql (pg_dump output) into structured files under database/schema/.

Parses pg_dump section markers (-- Name: ...; Type: ...; Schema: ...) and routes
each section to the appropriate output file. Skips functions, composite types
(types schema), and materialized views (hand-maintained in database/schema/views/).

Output structure:
    database/schema/
      extensions.sql           — schemas, extensions, SET preamble
      enums/                   — one file per enum type
      tables/
        artifacts/             — domain-grouped (agent.sql, scenario.sql, ...)
        entries/               — domain-grouped
        resources/             — domain-grouped
        junctions/             — domain-grouped
        connections/           — domain-grouped
      constraints.sql          — PKs, unique, check constraints
      indexes.sql              — indexes (excludes MV indexes)
      foreign_keys.sql         — FK constraints
      views/                   — hand-maintained MVs (not generated by this script)

Usage:
    python database/scripts/split_schema.py [path/to/schema.sql]

If no argument is given, defaults to database/schema.sql relative to this script.
"""

import os
import re
import shutil
import sys
from collections import defaultdict

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------

SECTION_RE = re.compile(
    r"^-- Name: (?P<name>.+?); Type: (?P<type>.+?); Schema: (?P<schema>.+?); Owner: .+$"
)

META_CMD_RE = re.compile(r"^\\(restrict|unrestrict)\b")

DUMP_COMMENT_RE = re.compile(
    r"^-- (PostgreSQL database dump|Dumped from|Dumped by|PostgreSQL database dump complete)"
)

AUTO_GEN_HEADER = "-- Auto-generated by split_schema.py — do not edit manually"

# Table suffix → subfolder name
TABLE_SUFFIXES = {
    "_artifact": "artifacts",
    "_entry": "entries",
    "_resource": "resources",
    "_junction": "junctions",
    "_connection": "connections",
}


# ---------------------------------------------------------------------------
# Section classification
# ---------------------------------------------------------------------------

def classify_section(sec_type: str, schema: str, name: str, body_lines: list[str]) -> str | None:
    """Return a category key or None to skip."""
    t = sec_type.strip()
    s = schema.strip()

    if t in ("SCHEMA", "EXTENSION"):
        return "extensions"

    if t == "COMMENT":
        if s == "-":
            return "extensions"
        if name.startswith("TABLE "):
            return "tables"
        return None

    if t == "TYPE":
        return "enums" if s == "public" else None

    if t == "TABLE":
        return "tables" if s == "public" else None

    if t == "CONSTRAINT":
        return "constraints" if s == "public" else None

    if t == "INDEX":
        if s != "public":
            return None
        for line in body_lines:
            stripped = line.strip()
            if stripped.upper().startswith("CREATE") and "INDEX" in stripped.upper():
                on_match = re.search(r"\bON\s+(?:public\.)?(\w+)", stripped, re.IGNORECASE)
                if on_match and on_match.group(1).endswith("_mv"):
                    return None
        return "indexes"

    if t == "FK CONSTRAINT":
        return "foreign_keys" if s == "public" else None

    return None


def get_table_suffix_and_domain(table_name: str) -> tuple[str, str]:
    """Extract (suffix_folder, domain_prefix) from table name.

    e.g. "agent_names_connection" → ("connections", "agent")
         "agent_artifact"        → ("artifacts", "agent")
    """
    for suffix, folder in TABLE_SUFFIXES.items():
        if table_name.endswith(suffix):
            prefix = table_name[: -len(suffix)]
            # Domain is the first word of the prefix
            domain = prefix.split("_")[0] if prefix else table_name
            return folder, domain
    # Fallback (shouldn't happen given all tables match)
    return "other", table_name.split("_")[0]


# ---------------------------------------------------------------------------
# Parsing
# ---------------------------------------------------------------------------

class Section:
    __slots__ = ("name", "sec_type", "schema", "header_lines", "body_lines")

    def __init__(self, name: str, sec_type: str, schema: str):
        self.name = name
        self.sec_type = sec_type
        self.schema = schema
        self.header_lines: list[str] = []
        self.body_lines: list[str] = []


def parse_sections(schema_path: str) -> tuple[list[str], list[Section]]:
    """Parse schema.sql into preamble lines and a list of sections."""
    preamble: list[str] = []
    sections: list[Section] = []
    current: Section | None = None
    in_preamble = True

    with open(schema_path, "r") as f:
        for raw_line in f:
            line = raw_line.rstrip("\n")

            if META_CMD_RE.match(line):
                continue
            if DUMP_COMMENT_RE.match(line):
                continue

            m = SECTION_RE.match(line)
            if m:
                in_preamble = False
                current = Section(m.group("name"), m.group("type"), m.group("schema"))
                current.header_lines.append(line)
                sections.append(current)
                continue

            if in_preamble:
                if line.strip() == "--":
                    continue
                preamble.append(line)
            elif current is not None:
                if not current.body_lines:
                    if line.strip() == "--" or line.strip() == "":
                        current.header_lines.append(line)
                        continue
                current.body_lines.append(line)

    return preamble, sections


def section_to_text(sec: Section) -> str:
    """Render a section back to text."""
    return "\n".join(sec.header_lines + sec.body_lines)


# ---------------------------------------------------------------------------
# Writing
# ---------------------------------------------------------------------------

def write_file(path: str, header: str, content: str) -> None:
    """Write a file with header comment."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(f"-- {header}\n")
        f.write(f"{AUTO_GEN_HEADER}\n")
        f.write("--\n\n")
        f.write(content)
        if not content.endswith("\n"):
            f.write("\n")


def write_split_files(schema_path: str, output_dir: str) -> dict[str, int]:
    """Parse schema.sql, classify sections, write split files."""

    # Clean output dir (except views/ which is hand-maintained)
    if os.path.exists(output_dir):
        for item in os.listdir(output_dir):
            if item == "views":
                continue
            item_path = os.path.join(output_dir, item)
            if os.path.isdir(item_path):
                shutil.rmtree(item_path)
            else:
                os.remove(item_path)
    os.makedirs(output_dir, exist_ok=True)

    preamble, sections = parse_sections(schema_path)
    counts: dict[str, int] = defaultdict(int)

    # Preamble
    preamble_stripped = list(preamble)
    while preamble_stripped and preamble_stripped[0].strip() == "":
        preamble_stripped.pop(0)
    while preamble_stripped and preamble_stripped[-1].strip() == "":
        preamble_stripped.pop()

    extensions_parts: list[str] = ["\n".join(preamble_stripped)]
    enum_parts: dict[str, list[str]] = {}
    # table_parts[suffix_folder][domain] = list of section texts
    table_parts: dict[str, dict[str, list[str]]] = defaultdict(lambda: defaultdict(list))
    constraints_parts: list[str] = []
    indexes_parts: list[str] = []
    fk_parts: list[str] = []

    orphan_sets: list[str] = []

    for sec in sections:
        key = classify_section(sec.sec_type, sec.schema, sec.name, sec.body_lines)

        if key is None:
            for line in sec.body_lines:
                if line.strip().upper().startswith("SET "):
                    orphan_sets.append(line)
            continue

        text = section_to_text(sec)

        orphan_prefix = ""
        if orphan_sets:
            orphan_prefix = "\n".join(orphan_sets) + "\n\n"
            orphan_sets = []

        if key == "extensions":
            extensions_parts.append(orphan_prefix + text)
            counts["extensions"] += 1

        elif key == "enums":
            enum_parts[sec.name] = [orphan_prefix + text]
            counts["enums"] += 1

        elif key == "tables":
            table_name = sec.name.replace("TABLE ", "") if sec.sec_type.strip() == "COMMENT" else sec.name
            suffix_folder, domain = get_table_suffix_and_domain(table_name)
            if orphan_prefix:
                table_parts[suffix_folder][domain].append(orphan_prefix.rstrip())
            table_parts[suffix_folder][domain].append(text)
            counts["tables"] += 1

        elif key == "constraints":
            if orphan_prefix:
                constraints_parts.append(orphan_prefix.rstrip())
            constraints_parts.append(text)
            counts["constraints"] += 1

        elif key == "indexes":
            if orphan_prefix:
                indexes_parts.append(orphan_prefix.rstrip())
            indexes_parts.append(text)
            counts["indexes"] += 1

        elif key == "foreign_keys":
            if orphan_prefix:
                fk_parts.append(orphan_prefix.rstrip())
            fk_parts.append(text)
            counts["foreign_keys"] += 1

    if orphan_sets:
        extensions_parts.append("\n".join(orphan_sets))

    # --- Write files ---

    write_file(
        os.path.join(output_dir, "extensions.sql"),
        "Extensions, schemas, and session settings",
        "\n\n".join(extensions_parts) + "\n",
    )

    # enums/ — one file per enum
    enums_dir = os.path.join(output_dir, "enums")
    os.makedirs(enums_dir, exist_ok=True)
    for enum_name in sorted(enum_parts):
        write_file(
            os.path.join(enums_dir, f"{enum_name}.sql"),
            f"Enum: {enum_name}",
            "\n\n".join(enum_parts[enum_name]) + "\n",
        )

    # tables/ — suffix folders with domain-grouped files
    tables_dir = os.path.join(output_dir, "tables")
    table_file_counts: dict[str, int] = {}
    for suffix_folder in sorted(table_parts):
        folder_path = os.path.join(tables_dir, suffix_folder)
        os.makedirs(folder_path, exist_ok=True)
        domains = table_parts[suffix_folder]
        table_file_counts[suffix_folder] = len(domains)
        for domain in sorted(domains):
            write_file(
                os.path.join(folder_path, f"{domain}.sql"),
                f"Tables: {domain}_*_{suffix_folder.rstrip('s')} (and variants)",
                "\n\n".join(domains[domain]) + "\n",
            )

    # Flat files
    write_file(
        os.path.join(output_dir, "constraints.sql"),
        "Primary key, unique, and check constraints",
        "\n\n".join(constraints_parts) + "\n",
    )
    write_file(
        os.path.join(output_dir, "indexes.sql"),
        "Indexes (excludes materialized view indexes)",
        "\n\n".join(indexes_parts) + "\n",
    )
    write_file(
        os.path.join(output_dir, "foreign_keys.sql"),
        "Foreign key constraints",
        "\n\n".join(fk_parts) + "\n",
    )

    return dict(counts), table_file_counts


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    db_dir = os.path.dirname(script_dir)

    schema_path = sys.argv[1] if len(sys.argv) > 1 else os.path.join(db_dir, "schema.sql")
    output_dir = os.path.join(db_dir, "schema")

    if not os.path.exists(schema_path):
        print(f"Error: {schema_path} not found", file=sys.stderr)
        sys.exit(1)

    print(f"Splitting {schema_path} → {output_dir}/")
    counts, table_file_counts = write_split_files(schema_path, output_dir)

    total = sum(counts.values())
    print(f"\nRouted {total} sections:")
    print(f"  extensions.sql          {counts.get('extensions', 0):5d} sections")
    print(f"  enums/                  {counts.get('enums', 0):5d} files (one per enum)")
    print(f"  tables/                 {counts.get('tables', 0):5d} tables:")
    for folder in sorted(table_file_counts):
        print(f"    {folder + '/':24s}{table_file_counts[folder]:3d} domain files")
    print(f"  constraints.sql         {counts.get('constraints', 0):5d} sections")
    print(f"  indexes.sql             {counts.get('indexes', 0):5d} sections")
    print(f"  foreign_keys.sql        {counts.get('foreign_keys', 0):5d} sections")

    views_dir = os.path.join(output_dir, "views")
    if os.path.exists(views_dir):
        view_count = sum(1 for _ in _rglob_sql(views_dir))
        print(f"  views/                  {view_count:5d} MV files (hand-maintained)")


def _rglob_sql(d: str):
    for root, _, files in os.walk(d):
        for f in files:
            if f.endswith(".sql"):
                yield os.path.join(root, f)


if __name__ == "__main__":
    main()
