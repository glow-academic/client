#!/usr/bin/env bash
set -euo pipefail

# Script to generate consolidated test schema SQL file
# This resolves all \i includes from app/init.sql into a single file

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
DB_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
OUTPUT_FILE="$DB_ROOT/../server/tests/test-schema.sql"
INIT_SQL="$DB_ROOT/app/init.sql"

echo "🔧 Generating test schema from database/app/init.sql..."

# Step 1: Generate all seed data from CS folder (agents, models, personas)
if [[ -f "$DB_ROOT/seed/init.sh" ]]; then
    echo "🌱 Generating all CS seed data (agents, models, personas)..."
    if "$DB_ROOT/seed/init.sh"; then
        echo "✅ All CS seed data generated successfully"
    else
        echo "⚠️  CS seed generation had issues, but continuing with existing SQL..."
    fi
else
    echo "⚠️  CS seed initialization script not found, using existing SQL"
fi

# Create output file with header
cat > "$OUTPUT_FILE" << 'EOF'
-- ============================================================================
-- CONSOLIDATED TEST SCHEMA
-- Auto-generated from database/app/init.sql
-- DO NOT EDIT THIS FILE DIRECTLY - run 'make generate-test-schema' to regenerate
-- ============================================================================

EOF

# Function to resolve \i includes recursively
resolve_includes() {
    local sql_file="$1"
    local base_dir="$(dirname "$sql_file")"
    
    # Read file and handle last line without newline
    while IFS= read -r line || [[ -n "$line" ]]; do
        # Check if line starts with \i (psql include directive)
        if [[ "$line" =~ ^[[:space:]]*\\i[[:space:]]+(.+) ]]; then
            local include_path="${BASH_REMATCH[1]}"
            # Remove any trailing comments or whitespace
            include_path=$(echo "$include_path" | sed 's/[[:space:]]*--.*$//' | xargs)
            
            # Resolve relative to database root
            local full_path="$DB_ROOT/$include_path"
            
            if [[ -f "$full_path" ]]; then
                echo "-- ========================================"
                echo "-- Included from: $include_path"
                echo "-- ========================================"
                echo ""
                
                # Recursively resolve includes in the included file
                resolve_includes "$full_path"
                
                echo ""
            else
                echo "-- WARNING: File not found: $include_path" >&2
            fi
        else
            # Output ALL lines (including comments) to preserve complete SQL
            echo "$line"
        fi
    done < "$sql_file"
}

# Generate the consolidated SQL
resolve_includes "$INIT_SQL" >> "$OUTPUT_FILE"

# Add footer
cat >> "$OUTPUT_FILE" << 'EOF'

-- ============================================================================
-- END OF CONSOLIDATED TEST SCHEMA
-- ============================================================================
EOF

echo "✅ Test schema generated: $(basename "$OUTPUT_FILE")"
echo "📍 Location: $OUTPUT_FILE"

# Show file size
FILE_SIZE=$(wc -l < "$OUTPUT_FILE" | xargs)
echo "📊 Lines: $FILE_SIZE"

